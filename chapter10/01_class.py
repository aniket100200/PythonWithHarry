class Employee:
    language="Py"
    salary=12345

harry=Employee()
harry.name="Harry"
print(harry.name,harry.language,harry.salary)


aniket=Employee()
aniket.name="Aniket"
print(aniket.name,aniket.language,aniket.salary)

# here 'name' is instance/object attribute and language and salary are class attributes as they directly belong to the class